require "user.options"
require "user.keymaps"
require "config.lazy"

if vim.fn.executable('win32yank.exe') == 1 then
  vim.opt.clipboard:append { 'unnamedplus' }
  vim.g.clipboard = {
    name = 'win32yank',
    copy = {
      ['+'] = 'win32yank.exe -i --crlf',
      ['*'] = 'win32yank.exe -i --crlf',
    },
    paste = {
      ['+'] = 'win32yank.exe -o --lf',
      ['*'] = 'win32yank.exe -o --lf',
    },
    cache_enabled = 0,
  }
end
